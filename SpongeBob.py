def draw_circle(r=0.5,xc=0,yc=0):
    glBegin(GL_POLYGON)
    for a in np.arange(0,2*pi,.001):
        x=xc+ r *cos(a)
        y=yc+ r *sin(a)
        glVertex(x,y)   
    glEnd()

def draw_circle_1(r=0.5,xc=0,yc=0):
    glBegin(GL_POLYGON)
    for a in np.arange(0,pi,.001):
        x=xc+ r *cos(a)
        y=yc+ r *sin(a)
        glVertex(-x,-y)   
    glEnd()   
    

